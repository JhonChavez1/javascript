// CICLO WHILE
// Imprimir los valoeres del 0 al 5

let contador = 0;
let repeticiones = 5;
while(contador <= repeticiones){
    console.log(contador++);
    //contador++;
}

// CICLO DO WHILE
let contador2 = 0;
let repeticiones2 = 5;
do{
    console.log(contador2++);
} while(contador2 <= repeticiones2);

// CICLO FOR
let repeticiones3 = 5;
for(let contador3 = 0; contador3 <= repeticiones3; contador3++){
    console.log(contador3); 
}

